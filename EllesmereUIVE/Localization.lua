local locale = GetLocale and GetLocale() or "enUS"
local selected = locale == "zhCN" and zhCN or (locale == "zhTW" and zhTW or enUS)

function NS.L(key, ...)
    local value = selected[key] or enUS[key] or key
    if select("#", ...) > 0 then
        local ok, formatted = pcall(string.format, value, ...)
        if ok then return formatted end
    end
    return value
end
